package model;

/**
 * Classe para armazenar os atributos e métodos do objeto Materia
 * @author Elisson Barbieri Machado
 * @since 23 de fev. de 2021
 */
public class Materia {
	
	// declarando os atributos do objeto Materia
	private int codigo;
	private String nome;
	
	
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	

}
